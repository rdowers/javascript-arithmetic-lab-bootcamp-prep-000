//add (a,b) = a + //

function add(a, b) {
  return a + b;
}

//subtract(a, b) subtracts b from a and returns the result:

function subtract(a, b) {
  return a - b;
}

//multiply(a, b) --> a * b

function multiply(a, b) {
  return a * b;
}

//divide(a, b) -> a/b

function divide(a, b) {
  return a / b;
}
